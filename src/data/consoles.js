// Helper to get games for a console, returns empty array if none defined yet
export function getGamesForConsole(consoleId) {
  return GAMES[consoleId] || [];
}

// Helper to find a specific game
export function getGame(consoleId, gameId) {
  const games = getGamesForConsole(consoleId);
  return games.find(g => g.id === gameId) || null;
}

// Helper to find a console by id
export function getConsole(consoleId) {
  return CONSOLES.find(c => c.id === consoleId) || null;
}
